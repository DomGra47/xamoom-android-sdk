package com.xamoom.xamoom_android_sdk.xamoom_android_sdk.api.request;

/**
 *
 */
public class Location {
    private double lat;
    private double lon;

    public Location(double lat, double lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
